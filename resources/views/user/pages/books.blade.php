@push('script')
    <script>
        $(document).ready(function() {
            $('.js-addCart').on('click', function(e) {
                console.log('12321');
                e.preventDefault();
                let productId = $(this).data('id');
                let productName = $(this).data('name');
                let productCode = $(this).data('code');
                let productPrice = $(this).data('price');
                let productImg = $(this).data('image');

                addToCart(productId, productName, productCode, productPrice, productImg)

                getTotalCart()

                $.toast({
                    heading: 'Thành công',
                    text: 'Thêm sách vào giỏ hàng thành công    ',
                    position: 'top-right',
                    stack: false,
                    icon: 'success'
                })
            });
        });

        //Add item to cart
        function addToCart(productId, productName, productCode, productPrice, productImg) {
            let currentCart = JSON.parse(sessionStorage.getItem('cart'));
            currentCart = currentCart == null ? [] : currentCart;
            for (let i = 0; i < currentCart.length; i++) {
                if (currentCart[i].id === productId) {
                    currentCart[i].qty++;
                    saveCart(currentCart);
                    return;
                }
            }

            currentCart.push({
                id: productId,
                name: productName,
                code: productCode,
                price: productPrice,
                image: productImg,
                qty: 1
            });
            saveCart(currentCart);
        }

        //Save cart
        function saveCart(cart) {
            cart = JSON.stringify(cart);
            return sessionStorage.setItem('cart', cart);
        }

        //Get total cart
        function getTotalCart() {
            let currentCart = JSON.parse(sessionStorage.getItem('cart'));
            currentCart = currentCart == null ? [] : currentCart;
            return $('.js-total-card').html(`<span>${currentCart.length}</span>`);
        }
    </script>
@endpush
